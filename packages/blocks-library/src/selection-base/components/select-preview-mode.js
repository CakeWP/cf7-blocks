/**
 * WordPress Dependencies
 *
 */
import { isEmpty } from 'lodash';
import { __ } from '@wordpress/i18n';

function SelectPreviewMode( props ) {
	const {
		id,
		name,
		items,
		isRequired,
		isMultipleSelect,
		autogeneratedName,
	} = props;
	return (
		<select
			id={ id }
			className="cf7blocks-selection-base-select-block"
			name={ isEmpty( name ) ? autogeneratedName : name }
			required={ isRequired }
			multiple={ isMultipleSelect }
		>
			{ items.map( ( item, index ) => {
				return (
					<option key={ index } value={ item.label }>
						{ item.label }
					</option>
				);
			} ) }
		</select>
	);
}

export default SelectPreviewMode;
